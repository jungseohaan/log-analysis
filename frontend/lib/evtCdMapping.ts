/**
 * evtCd를 설명으로 변환하는 함수
 * 매핑에 없으면 가장 유사한 것을 찾거나 원본을 반환
 */
export function getEvtCdDescription(evtCd: string): string {
  if (!evtCd) return "";

  // 정확히 일치하는 경우
  if (evtCdMap[evtCd]) {
    return evtCdMap[evtCd];
  }

  // 유사한 것 찾기 (접두사 제거 후 매칭)
  // 예: SM-COM2 -> TC-COM2, SE-ASS -> TC-ASS 등
  const patterns = [
    // 접두사를 TC로 변환해서 찾기
    evtCd.replace(/^(SM-|SE-|TM-|TE-)/, "TC-"),
    // 접두사를 SC로 변환해서 찾기
    evtCd.replace(/^(SM-|SE-|TM-|TE-)/, "SC-"),
    // 접두사를 CC로 변환해서 찾기
    evtCd.replace(/^(CE-)/, "CC-"),
  ];

  for (const pattern of patterns) {
    if (evtCdMap[pattern]) {
      return evtCdMap[pattern];
    }
  }

  // 매핑을 찾지 못한 경우 원본 반환
  return evtCd;
}
